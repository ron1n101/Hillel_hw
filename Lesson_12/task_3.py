"""
3. Написать функцию, которая принимает в качестве параметра целое, многозначное число.
 Функция должна вернуть сумму произведений каждой цифры на её порядковый номер. Например:
	есть число 874658734
	8 именн порядковый номер 1,
	7 - 2
	4 - 3
	6 - 4
	5 - 5 и т. д.
	необходимо посчитать: 8 * 1 + 7 * 2 + 4 * 3 + 6 * 4 + 5 * 5 .....
	Задачу надо решить с использование list comprehension и функции sum() в ОДНУ строку.

"""
n = input('Введите многозначное число: ')
def summa(n):
    return sum([int(str(n)[i]) * (i + 1) for i in range(len(str(n)))])
print(summa(n))